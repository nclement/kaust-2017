#include <iostream>

using namespace std;

void cupPlate() {
    // Draw the cup and plate
    cout << "\\        /" << endl;
    cout << " \\______/" << endl;
    cout << "+--------+" << endl;
    cout << "\n";
}

void cap() {
    // Draw the cap
    cout << "  ++++++" << endl;
    cout << " /      \\" << endl;
    cout << "/        \\" << endl;
    cout << "\n";
}

int main() {
    cap();

    // Draw the cup and plate
    cout << "\\        /" << endl;
    cout << " \\______/" << endl;
    cout << "+--------+" << endl;
    cout << "\n";
    // Draw the Stop Sign
    cout << "  ++++++" << endl;
    cout << " /      \\" << endl;
    cout << "/        \\" << endl;
    cout << "|  STOP  |" << endl;
    cout << "\\        /" << endl;
    cout << " \\______/" << endl;
    cout << "\n";
    // Draw the hat
    cout << "  ++++++" << endl;
    cout << " /      \\" << endl;
    cout << "/        \\" << endl;
    cout << "+--------+" << endl;
    return 0;
}